package bean;

public class TranDau {

	private int idTranDau;
	private int SoLuongToiDa;
	private int SoLuongHienCo;
	private Thue thue;
	
	public int getIdTranDau() {
		return idTranDau;
	}
	public void setIdTranDau(int idTranDau) {
		this.idTranDau = idTranDau;
	}
	public int getSoLuongToiDa() {
		return SoLuongToiDa;
	}
	public void setSoLuongToiDa(int soLuongToiDa) {
		SoLuongToiDa = soLuongToiDa;
	}
	public int getSoLuongHienCo() {
		return SoLuongHienCo;
	}
	public void setSoLuongHienCo(int soLuongHienCo) {
		SoLuongHienCo = soLuongHienCo;
	}
	public Thue getThue() {
		return thue;
	}
	public void setThue(Thue thue) {
		this.thue = thue;
	}
}
