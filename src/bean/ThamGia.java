package bean;

public class ThamGia {

	private int idThamGia;
	private NguoiDung nguoiDung;
	private TranDau tranDau;
	private int soLuongThamGia;
	public int getIdThamGia() {
		return idThamGia;
	}
	public void setIdThamGia(int idThamGia) {
		this.idThamGia = idThamGia;
	}
	public NguoiDung getNguoiDung() {
		return nguoiDung;
	}
	public void setNguoiDung(NguoiDung nguoiDung) {
		this.nguoiDung = nguoiDung;
	}
	public TranDau getTranDau() {
		return tranDau;
	}
	public void setTranDau(TranDau tranDau) {
		this.tranDau = tranDau;
	}
	public int getSoLuongThamGia() {
		return soLuongThamGia;
	}
	public void setSoLuongThamGia(int soLuongThamGia) {
		this.soLuongThamGia = soLuongThamGia;
	}
	
	
}
